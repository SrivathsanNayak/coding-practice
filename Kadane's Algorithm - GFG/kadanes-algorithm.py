class Solution:
    ##Complete this function
    #Function to find the sum of contiguous subarray with maximum sum.
    def maxSubArraySum(self,arr,N):
        #Kadane's algo
        currSum = bestSum = arr[0]
        for i in range(1, N):
            currSum = max(arr[i], arr[i] + currSum)
            bestSum = max(currSum, bestSum)
        return bestSum

